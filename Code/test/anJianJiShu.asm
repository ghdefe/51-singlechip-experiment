ORG 00H
START:
		MOV DPTR,#TABLE
		MOV R0,#00H
		MOV P0,#0C0H
		MOV P2,#0F7H



S1:
		INC R0
		CJNE R0,#10,S2
		MOV R0,#00H

S2:
		JB P3.2,$
		LCALL DELAY
		JB P3.2,S2

S3:
		MOV A,R0
		MOVC A,@A+DPTR
		MOV P0,A

S4:
		JB P3.2,S1
		JMP S4


DELAY:
		MOV R5,#200
D1:
		MOV R6,#250
		DJNZ R6,$
		DJNZ R5,D1
		RET

TABLE:
		DB	0C0H,0F9H,0A4H,0B0H,099H,092H,082H,0F8H,080H,090H

END			
